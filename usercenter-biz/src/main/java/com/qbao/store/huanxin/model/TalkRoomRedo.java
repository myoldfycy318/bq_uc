package com.qbao.store.huanxin.model;

public class TalkRoomRedo {
	private String action;
	private Boolean description;
	private String id;
	private Boolean maxusers;
	private Boolean name;
	private String[]newmembers;
	private Boolean result;
	private Boolean success;
	private String user;
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	public Boolean getDescription() {
		return description;
	}
	public void setDescription(Boolean description) {
		this.description = description;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Boolean getMaxusers() {
		return maxusers;
	}
	public void setMaxusers(Boolean maxusers) {
		this.maxusers = maxusers;
	}
	public Boolean getName() {
		return name;
	}
	public void setName(Boolean name) {
		this.name = name;
	}
	public String[] getNewmembers() {
		return newmembers;
	}
	public void setNewmembers(String[] newmembers) {
		this.newmembers = newmembers;
	}
	public Boolean getResult() {
		return result;
	}
	public void setResult(Boolean result) {
		this.result = result;
	}
	public Boolean getSuccess() {
		return success;
	}
	public void setSuccess(Boolean success) {
		this.success = success;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
}
