public class ClientEntity 
{
    /**
     * clinetID
     */
    private String clientId;
    
    /**
     * clientName
     */
    private String clientName;
    
    /**
     * buId
     */
    private String buId;
    
    /**
     * clinetType
     */
    private Integer clientType;
    
    /**
     * 秘钥
     */
    private String clientSecret;
    
    /***
     * 第三方描述
     */
    private String clientDesc;
    
    /**
     * 第三方回调地址
     */
    private String callbackUrl;
   
    /**
     * 创建时间
     */
    private Date createTime;
    
    /**
     * 更新时间
     */
    private Date updateTime;
    
    


	public String getClientId() {
		return clientId;
	}


	public void setClientId(String clientId) {
		this.clientId = clientId;
	}


	public String getClientName() {
		return clientName;
	}


	public Integer getClientType() {
		return clientType;
	}


	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	
	public String getBuId() {
		return buId;
	}


	public void setBuId(String buId) {
		this.buId = buId;
	}


	public void setClientType(Integer clientType) {
		this.clientType = clientType;
	}


	public String getClientSecret() {
		return clientSecret;
	}


	public void setClientSecret(String clientSecret) {
		this.clientSecret = clientSecret;
	}


	public String getClientDesc() {
		return clientDesc;
	}


	public void setClientDesc(String clientDesc) {
		this.clientDesc = clientDesc;
	}


	public String getCallbackUrl() {
		return callbackUrl;
	}


	public void setCallbackUrl(String callbackUrl) {
		this.callbackUrl = callbackUrl;
	}


	public Date getCreateTime() {
        return createTime;
    }

    
    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    
    public Date getUpdateTime() {
        return updateTime;
    }

    
    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
    
    public String toString()
    {
    	return "ClientEntity[" + "clientId="+clientId + ",clientName=" + clientName + ",buId=" + buId 
    			+ ",clientType=" + clientType + ",clientSecret=" + clientSecret + ",clientDesc=" + clientDesc 
    			+ ",callbackUrl=" + callbackUrl + ",createTime=" + createTime + ",updateTime=" + updateTime + "]";
    }
    
}
