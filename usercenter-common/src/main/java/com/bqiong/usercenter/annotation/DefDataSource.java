package com.bqiong.usercenter.annotation;

import java.lang.annotation.*;

/**
 * 默认数据源注解
 * DefDataSource
 *
 * @author Zhang ShanMin
 * @date 2017/7/19
 * @time 17:36
 */
@Inherited
@Target({ElementType.METHOD})
@Retention(RetentionPolicy.RUNTIME)
@Documented
public @interface DefDataSource {

    String datasource() default "dataSource01";
}
